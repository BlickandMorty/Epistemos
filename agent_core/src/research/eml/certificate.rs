//! # Lean 4 certificate emission
//!
//! This module emits Lean 4 source as a String targeting
//! `Epistemos.EML`. The committed schema module built with explicit
//! `~/.elan/bin` PATH at iter-593, with zero sorries in committed
//! Lean sources. The emitter's live obligation is schema-shaped
//! Lean source for the branch-safety, evaluator equality, and
//! positive-value proof rows.
//!
//! The emitted term for a tree is:
//!
//! - `(1 : ℝ)` for an [`super::grammar::EmlExpr::One`] leaf.
//! - `(Real.exp <left> - Real.log <right>)` for an `Eml(l, r)`.
//!
//! The full certificate for a [`super::branched::PositiveEmlExpr`]
//! is a Lean source block targeting `Epistemos.EML.CertificateTarget`:
//!
//! ```text
//! -- Generated by EML-IR certificate.rs (iter-13)
//! import Epistemos.EML
//! namespace Epistemos.EML.Generated
//! noncomputable def eml_expr_<hash> : Epistemos.EML.Expr := ...
//! theorem eml_branch_safe_<hash> :
//!     Epistemos.EML.BranchSafe eml_expr_<hash> := by
//!   exact Epistemos.EML.one_branch_safe
//! noncomputable def eml_certificate_<hash> :
//!     Epistemos.EML.CertificateTarget := ...
//! ```
//!
//! Symbolic branch-safety and evaluator equalities close from schema
//! lemmas where available. Non-symbolic branch-safety or evaluator
//! equalities are exposed as explicit runtime witness binders rather
//! than hidden generated proof bodies.
//!
//! ## What the emitter guarantees today
//!
//! - **Schema targeting.** The emitted source imports
//!   `Epistemos.EML` and constructs an `Epistemos.EML.Expr` plus
//!   `Epistemos.EML.CertificateTarget`.
//! - **Round-trip-with-evaluator.** The emitted cached value
//!   matches `PositiveEmlExpr::value()` exactly.
//! - **Determinism.** Same input → same emitted string (no
//!   floating-point formatting drift in the term portion).

use super::branched::PositiveEmlExpr;
use super::grammar::EmlExpr;

/// Lean 4 term for an [`EmlExpr`] subtree.
///
/// Recursive: `One` lowers to `(1 : ℝ)`; `Eml(l, r)` lowers to
/// `(Real.exp <l-term> - Real.log <r-term>)`.
pub fn lean_term(expr: &EmlExpr) -> String {
    match expr {
        EmlExpr::One => "(1 : ℝ)".to_string(),
        EmlExpr::Eml(l, r) => {
            format!(
                "(Real.exp {} - Real.log {})",
                lean_term(l),
                lean_term(r)
            )
        }
    }
}

/// Lean schema constructor term for an [`EmlExpr`] subtree.
///
/// This targets `lean/Epistemos/Epistemos/EML.lean`, not the
/// real-valued semantic expression. Generated certificates use this
/// term to construct `Epistemos.EML.Expr` values.
pub fn lean_expr_term(expr: &EmlExpr) -> String {
    match expr {
        EmlExpr::One => "Epistemos.EML.Expr.one".to_string(),
        EmlExpr::Eml(l, r) => {
            format!(
                "(Epistemos.EML.Expr.eml {} {})",
                lean_expr_term(l),
                lean_expr_term(r)
            )
        }
    }
}

/// Stable per-tree hash for the certificate name. Uses Rust's
/// default `Hasher` indirectly via `format!`-based serialization
/// of the bare expression (deterministic since `EmlExpr` is a
/// simple ADT with `#[derive(Debug)]`).
fn tree_hash_suffix(expr: &EmlExpr) -> String {
    // Stable structural hash: walk the tree and accumulate an
    // FNV-1a 64-bit hash of the canonical "1" / "E" symbols.
    // FNV-1a chosen for determinism + no_std-friendliness.
    const FNV_OFFSET: u64 = 0xcbf29ce484222325;
    const FNV_PRIME: u64 = 0x100000001b3;
    fn step(expr: &EmlExpr, h: &mut u64) {
        match expr {
            EmlExpr::One => {
                *h ^= b'1' as u64;
                *h = h.wrapping_mul(FNV_PRIME);
            }
            EmlExpr::Eml(l, r) => {
                *h ^= b'E' as u64;
                *h = h.wrapping_mul(FNV_PRIME);
                step(l, h);
                *h ^= b',' as u64;
                *h = h.wrapping_mul(FNV_PRIME);
                step(r, h);
                *h ^= b')' as u64;
                *h = h.wrapping_mul(FNV_PRIME);
            }
        }
    }
    let mut h = FNV_OFFSET;
    step(expr, &mut h);
    format!("{:016x}", h)
}

fn indent_lean_term_by(term: &str, spaces: usize) -> String {
    let pad = " ".repeat(spaces);
    term.lines()
        .map(|line| format!("{pad}{line}"))
        .collect::<Vec<_>>()
        .join("\n")
}

fn positive_eval_proof_source(expr: &EmlExpr) -> Option<String> {
    match expr {
        EmlExpr::One => Some("(by norm_num [Epistemos.EML.Expr.eval])".to_string()),
        EmlExpr::Eml(l, r) if matches!(&**r, EmlExpr::One) => Some(format!(
            "(Epistemos.EML.eval_eml_right_one_positive {})",
            lean_expr_term(l)
        )),
        EmlExpr::Eml(_, _) => None,
    }
}

fn closed_branch_safe_term(expr: &EmlExpr) -> Option<String> {
    match expr {
        EmlExpr::One => Some("Epistemos.EML.one_branch_safe".to_string()),
        EmlExpr::Eml(l, r) => {
            let left_expr = lean_expr_term(l);
            let right_expr = lean_expr_term(r);
            let left_safe = indent_lean_term_by(&closed_branch_safe_term(l)?, 6);
            let right_safe = indent_lean_term_by(&closed_branch_safe_term(r)?, 6);
            let right_positive = indent_lean_term_by(&positive_eval_proof_source(r)?, 6);
            Some(format!(
                "(Epistemos.EML.BranchObligation.discharge\n\
                 \x20 {{ left := {left_expr}\n\
                 \x20   right := {right_expr}\n\
                 \x20   left_safe :=\n\
                 {left_safe}\n\
                 \x20   right_safe :=\n\
                 {right_safe}\n\
                 \x20   right_positive :=\n\
                 {right_positive} }})"
            ))
        }
    }
}

fn branch_safe_proof_source(expr: &EmlExpr) -> String {
    closed_branch_safe_term(expr).map_or_else(
        || {
            "exact branchWitness.branch_safe"
                .to_string()
        },
        |term| format!("exact {term}"),
    )
}

fn needs_runtime_branch_witness(expr: &EmlExpr) -> bool {
    closed_branch_safe_term(expr).is_none()
}

fn runtime_branch_witness_binder(expr: &EmlExpr, suffix: &str) -> String {
    if needs_runtime_branch_witness(expr) {
        format!(
            "\n\x20   (branchWitness : Epistemos.EML.RuntimeBranchSafeWitness eml_expr_{suffix})"
        )
    } else {
        String::new()
    }
}

fn runtime_branch_witness_arg(expr: &EmlExpr) -> &'static str {
    if needs_runtime_branch_witness(expr) {
        " branchWitness"
    } else {
        ""
    }
}

fn eval_matches_proof_source(expr: &EmlExpr) -> &'static str {
    match expr {
        EmlExpr::One => "exact Epistemos.EML.Expr.eval_one",
        EmlExpr::Eml(_, _) => "exact evalWitness.value_matches",
    }
}

fn needs_runtime_eval_witness(expr: &EmlExpr) -> bool {
    !matches!(expr, EmlExpr::One)
}

fn runtime_eval_witness_binder(expr: &EmlExpr, suffix: &str) -> String {
    if needs_runtime_eval_witness(expr) {
        format!(
            "\n\x20   (evalWitness : Epistemos.EML.RuntimeEvalWitness eml_expr_{suffix} eml_value_{suffix})"
        )
    } else {
        String::new()
    }
}

fn runtime_eval_witness_arg(expr: &EmlExpr) -> &'static str {
    if needs_runtime_eval_witness(expr) {
        " evalWitness"
    } else {
        ""
    }
}

/// Full Lean 4 certificate for a runtime-validated tree. The
/// theorem statement asserts the tree's value is strictly positive;
/// branch-safety closes from schema lemmas where possible, and
/// non-leaf evaluator equality is supplied as a RuntimeEvalWitness.
pub fn lean_certificate(p: &PositiveEmlExpr) -> String {
    let expr_term = lean_expr_term(p.as_expr());
    let semantic_term = lean_term(p.as_expr());
    let suffix = tree_hash_suffix(p.as_expr());
    let value = p.value();
    let branch_safe_proof = branch_safe_proof_source(p.as_expr());
    let branch_witness_binder = runtime_branch_witness_binder(p.as_expr(), &suffix);
    let branch_witness_arg = runtime_branch_witness_arg(p.as_expr());
    let eval_matches_proof = eval_matches_proof_source(p.as_expr());
    let eval_witness_binder = runtime_eval_witness_binder(p.as_expr(), &suffix);
    let eval_witness_arg = runtime_eval_witness_arg(p.as_expr());
    format!(
        "-- Generated by EML-IR certificate.rs (T5 Phase B1 iter-13; Lean-first iter-607)\n\
         -- Source: docs/fusion/PRIMITIVE_IR_STACK_DOCTRINE_2026_05_17.md §3\n\
         -- Schema: lean/Epistemos/Epistemos/EML.lean\n\
         -- Schema module built with explicit ~/.elan/bin PATH at iter-593.\n\
         -- Generated non-leaf eval proofs close from RuntimeEvalWitness.\n\
         -- Generated non-symbolic branch proofs close from RuntimeBranchSafeWitness.\n\
         -- Runtime-validated value: {value}\n\
         -- Semantic term: {semantic_term}\n\
         import Epistemos.EML\n\
         \n\
         namespace Epistemos.EML.Generated\n\
         \n\
         noncomputable def eml_expr_{suffix} : Epistemos.EML.Expr :=\n\
         \x20   {expr_term}\n\
         \n\
         noncomputable def eml_value_{suffix} : ℝ :=\n\
         \x20   {value}\n\
         \n\
         theorem eml_branch_safe_{suffix}{branch_witness_binder} :\n\
         \x20   Epistemos.EML.BranchSafe eml_expr_{suffix} := by\n\
         \x20 {branch_safe_proof}\n\
         \n\
         theorem eml_eval_matches_{suffix}{eval_witness_binder} :\n\
         \x20   Epistemos.EML.Expr.eval eml_expr_{suffix} = eml_value_{suffix} := by\n\
         \x20 {eval_matches_proof}\n\
         \n\
         theorem eml_positive_value_{suffix} :\n\
         \x20   0 < eml_value_{suffix} := by\n\
         \x20 norm_num [eml_value_{suffix}]\n\
         \n\
         noncomputable def eml_certificate_{suffix}{branch_witness_binder}{eval_witness_binder} : Epistemos.EML.CertificateTarget :=\n\
         \x20   {{ expr := eml_expr_{suffix}\n\
         \x20     value := eml_value_{suffix}\n\
         \x20     branch_safe := eml_branch_safe_{suffix}{branch_witness_arg}\n\
         \x20     value_matches := eml_eval_matches_{suffix}{eval_witness_arg}\n\
         \x20     positive_value := eml_positive_value_{suffix} }}\n\
         \n\
         theorem eml_eval_positive_{suffix}{branch_witness_binder}{eval_witness_binder} :\n\
         \x20   0 < Epistemos.EML.Expr.eval eml_expr_{suffix} := by\n\
         \x20 exact Epistemos.EML.CertificateTarget.eval_positive (eml_certificate_{suffix}{branch_witness_arg}{eval_witness_arg})\n\
         \n\
         end Epistemos.EML.Generated\n",
        value = value,
        suffix = suffix,
        expr_term = expr_term,
        semantic_term = semantic_term,
        branch_safe_proof = branch_safe_proof,
        branch_witness_binder = branch_witness_binder,
        branch_witness_arg = branch_witness_arg,
        eval_matches_proof = eval_matches_proof,
        eval_witness_binder = eval_witness_binder,
        eval_witness_arg = eval_witness_arg,
    )
}

