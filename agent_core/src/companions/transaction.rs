        // No longer in active list, still in all list.
        assert!(registry.list_active().unwrap().is_empty());
        assert_eq!(registry.list_all().unwrap().len(), 1);
        // Activity tracker dropped the entry.
        assert_eq!(registry.activity().state(companion.id), None);
