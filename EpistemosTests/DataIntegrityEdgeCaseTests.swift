@Suite("Data Integrity - Circular References")
@MainActor
struct DataIntegrityCircularReferenceTests {
    
    @Test("Self-loop edge")
    func selfLoopEdge() {
        let store = GraphStore()
        
        store.addNode(makeNode(id: "self"))
        
        let selfEdge = GraphEdgeRecord(
            id: "self-loop",
            sourceNodeId: "self",
            targetNodeId: "self",
            type: .reference,
            weight: 1.0,
            createdAt: .now
        )
        store.addEdge(selfEdge)
        
        // Self-loop creates odd adjacency
        let neighbors = store.neighbors(of: "self")
        #expect(neighbors.count == 1) // Self is neighbor
        #expect(store.adjacency["self"]?.contains("self") == true)
    }
    
    @Test("Mutual references")
    func mutualReferences() {
        let store = GraphStore()
        
        store.addNode(makeNode(id: "a"))
        store.addNode(makeNode(id: "b"))
        
        // a -> b
        store.addEdge(makeEdge(id: "e1", source: "a", target: "b"))
        // b -> a
        store.addEdge(makeEdge(id: "e2", source: "b", target: "a"))
        
        // Both should have each other as neighbors
        #expect(store.neighbors(of: "a").count == 1)
        #expect(store.neighbors(of: "b").count == 1)
    }
    
    @Test("Triangle cycle")
    func triangleCycle() {
        let store = GraphStore()
        
        for id in ["a", "b", "c"] {
            store.addNode(makeNode(id: id))
        }
        
        // a -> b -> c -> a
        store.addEdge(makeEdge(source: "a", target: "b"))
        store.addEdge(makeEdge(source: "b", target: "c"))
        store.addEdge(makeEdge(source: "c", target: "a"))
        
        // BFS should handle cycle without infinite loop
        let connected = store.connected(to: "a", maxDepth: 10)
        #expect(connected.count == 3)
    }
    
    @Test("Complex cyclic graph")
    func complexCyclicGraph() {
        let store = GraphStore()
        
        // Create multiple cycles
        for id in ["a", "b", "c", "d", "e"] {
            store.addNode(makeNode(id: id))
        }
        
        // Cycle 1: a-b-c-a
        store.addEdge(makeEdge(source: "a", target: "b"))
        store.addEdge(makeEdge(source: "b", target: "c"))
        store.addEdge(makeEdge(source: "c", target: "a"))
        
        // Cycle 2: c-d-e-c
        store.addEdge(makeEdge(source: "c", target: "d"))
        store.addEdge(makeEdge(source: "d", target: "e"))
        store.addEdge(makeEdge(source: "e", target: "c"))
        
        // Shortest path should handle cycles
        let path = store.query(.pathBetween(from: "a", to: "e", maxHops: 10))
        #expect(!path.isEmpty)
    }
    
    private func makeNode(id: String) -> GraphNodeRecord {
        GraphNodeRecord(
            id: id, type: .note, label: id, sourceId: nil,
            metadata: GraphNodeMetadata(), weight: 1.0,
            createdAt: .now, position: .zero, velocity: .zero
        )
    }
    
    private func makeEdge(id: String = UUID().uuidString, source: String, target: String) -> GraphEdgeRecord {
        GraphEdgeRecord(
            id: id, sourceNodeId: source, targetNodeId: target,
            type: .reference, weight: 1.0, createdAt: .now
        )
    }
}

