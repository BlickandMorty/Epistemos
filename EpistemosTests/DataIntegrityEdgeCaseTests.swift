@Suite("Data Integrity - String Edge Cases")
@MainActor
struct DataIntegrityStringTests {
    
    @Test("Null bytes in strings")
    func nullBytesInStrings() {
        let label = "Hello\0World"
        let node = SDGraphNode(type: .note, label: label)
        
        #expect(node.label.contains("\0"))
        
        // C string conversion should handle null bytes
        let cString = label.cString(using: .utf8)
        #expect(cString != nil)
    }
    
    @Test("Maximum string length")
    func maximumStringLength() {
        let maxLabel = String(repeating: "A", count: 100_000)
        let node = SDGraphNode(type: .note, label: maxLabel)
        
        #expect(node.label.count == 100_000)
    }
    
    @Test("Invalid UTF-8 sequences")
    func invalidUTF8() {
        // Invalid UTF-8 bytes
        let invalidBytes: [UInt8] = [0xC0, 0x80, 0xFF, 0xFE]
        
        // Creating string from invalid bytes
        if let invalidString = String(bytes: invalidBytes, encoding: .utf8) {
            let node = SDGraphNode(type: .note, label: invalidString)
            _ = node.label
        }
        
        // Fails gracefully if encoding fails
        #expect(String(bytes: invalidBytes, encoding: .utf8) == nil)
    }
    
    @Test("BMP and non-BMP characters")
    func bmpNonBmpCharacters() {
        // BMP: Basic Multilingual Plane (U+0000 to U+FFFF)
        // Non-BMP: Supplementary planes (U+10000+)
        
        let bmp = "ABC"
        let nonBmp = "𐍈𐌰𐍄𐌰𐌹" // Gothic letters
        let emoji = "👨‍👩‍👧‍👦" // Family emoji
        
        let node1 = SDGraphNode(type: .note, label: bmp)
        let node2 = SDGraphNode(type: .note, label: nonBmp)
        let node3 = SDGraphNode(type: .note, label: emoji)
        
        #expect(node1.label == bmp)
        #expect(node2.label == nonBmp)
        #expect(node3.label == emoji)
    }
    
    @Test("Surrogate pairs")
    func surrogatePairs() {
        // UTF-16 surrogate pairs are NOT valid Unicode scalars in Swift.
        // Test with a valid supplementary plane character instead (emoji).
        let supplementary = "\u{1F600}"  // Grinning face

        let node = SDGraphNode(type: .note, label: supplementary)
        #expect(!node.label.isEmpty)
        #expect(node.label == supplementary)
    }
}

