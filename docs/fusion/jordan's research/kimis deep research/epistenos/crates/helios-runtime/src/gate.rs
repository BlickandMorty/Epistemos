/// A claim entering the Resonance Gate for evaluation.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Claim {
    pub id: [u8; 32],
    pub proposition: String,
    pub confidence: f32,
    pub evidence_count: usize,
    pub is_observed: bool,
    pub source_residency: Residency,
}

/// The cognitive context against which a claim is evaluated.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct Context {
    pub active_claims: Vec<[u8; 32]>,
    pub feature_vector: Vec<f32>,
    pub current_residency: Residency,
    pub learning_mode: LearningMode,
}

// ---------------------------------------------------------------------------
// compute_resonance — full 8-field signature computation
// ---------------------------------------------------------------------------

/// Compute the complete 8-field Resonance Signature for a claim.
///
/// This is a **deterministic, pure function** — the same `(claim, context)`
/// pair always produces the same signature.
#[instrument(skip(claim, context), fields(claim_id = hex::encode(&claim.id)))]
pub fn compute_resonance(claim: &Claim, context: &Context) -> ResonanceSignature {
    // 1. tau — ternary state: does the claim fit the context?
    let tau = compute_tau(claim, context);

    // 2. rho — resonance strength: cosine similarity with context features
    let rho = compute_rho(claim, context);

    // 3. eta — evidence mass: fraction of evidence capacity used
    let eta = compute_eta(claim, context);

    // 4. kappa — KAM stability: does adding this claim preserve manifold structure?
    let kappa = compute_kappa(claim, context, rho, eta);

    // 5. delta — direction based on tau + rho + kappa
    let delta = compute_delta(tau, rho, kappa, eta);

    // 6. pi — primality: observed = Prime, inferred = Composite
    let pi = if claim.is_observed {
        Primality::Prime
    } else {
        Primality::Composite
    };

    // 7. lambda — target residency (may differ from source)
    let lambda = compute_lambda(claim.source_residency, delta, kappa);

    // 8. learning_mode — from context
    let learning_mode = context.learning_mode;

    let sig = ResonanceSignature {
        tau,
        delta,
        pi,
        rho: rho.clamp(0.0, 1.0),
        kappa: kappa.clamp(0.0, 1.0),
        eta: eta.clamp(0.0, 1.0),
        lambda,
        learning_mode,
    };

    info!(
        composite = sig.composite_scalar(),
        ?tau,
        ?delta,
        ?pi,
        rho,
        kappa,
        eta,
        ?lambda,
        ?learning_mode,
        "computed resonance signature"
    );

    sig
}

fn compute_tau(claim: &Claim, context: &Context) -> TernaryState {
    let overlap = context
        .active_claims
        .iter()
        .filter(|&c| *c == claim.id)
        .count();
    if overlap > 0 && claim.confidence > 0.7 {
        TernaryState::Pos
    } else if claim.confidence > 0.3 {
        TernaryState::Zero
    } else {
        TernaryState::Neg
    }
}

fn compute_rho(claim: &Claim, context: &Context) -> f32 {
    if context.feature_vector.is_empty() {
        return 0.5;
    }
    // Cosine similarity between claim confidence (as a scalar feature)
    // and the mean of the context feature vector.
    let mean_feature: f32 = context.feature_vector.iter().sum::<f32>() / context.feature_vector.len() as f32;
    let a = claim.confidence;
    let b = mean_feature;
    let dot = a * b;
    let norm_a = a.abs();
    let norm_b = b.abs();
    if norm_a == 0.0 || norm_b == 0.0 {
        0.0
    } else {
        let sim = dot / (norm_a * norm_b);
        (sim + 1.0) / 2.0 // map [-1,1] → [0,1]
    }
}

fn compute_eta(claim: &Claim, _context: &Context) -> f32 {
    // Evidence mass = evidence_count / (evidence_count + 10) with a soft ceiling.
    let count = claim.evidence_count as f32;
    count / (count + 10.0)
}

fn compute_kappa(_claim: &Claim, _context: &Context, rho: f32, eta: f32) -> f32 {
    // KAM stability heuristic: high coherence + sufficient evidence → stable manifold.
    // We model this as a soft-min of rho and eta, boosted when both are high.
    let base = rho.min(eta);
    let boost = (rho * eta).sqrt();
    (base + boost) / 2.0
}

fn compute_delta(tau: TernaryState, rho: f32, kappa: f32, eta: f32) -> Direction {
    match tau {
        TernaryState::Pos => {
            if rho > 0.75 && kappa > 0.6 && eta > 0.3 {
                Direction::Promote
            } else {
                Direction::Hold
            }
        }
        TernaryState::Zero => {
            if rho > 0.85 && kappa > 0.8 {
                Direction::Promote
            } else {
                Direction::Hold
            }
        }
        TernaryState::Neg => {
            if rho < 0.2 && kappa < 0.3 {
                Direction::Demote
            } else {
                Direction::Hold
            }
        }
    }
}

fn compute_lambda(current: Residency, delta: Direction, kappa: f32) -> Residency {
    let level = current.level();
    let new_level = match delta {
        Direction::Promote => (level + 1).min(5),
        Direction::Demote => level.saturating_sub(1),
        Direction::Hold => level,
    };
    // KAM stability bonus: if kappa > 0.9, allow an extra promotion step
    let bonus = if kappa > 0.9 && delta == Direction::Promote { 1 } else { 0 };
    let final_level = (new_level + bonus).min(5);
    match final_level {
        0 => Residency::L0,
        1 => Residency::L1,
        2 => Residency::L2,
        3 => Residency::L3,
        4 => Residency::L4,
        5 => Residency::LSe,
        _ => Residency::L0,
    }
}

