// gate_action — threshold-based gate decision
// ---------------------------------------------------------------------------

/// Threshold configuration for the Resonance Gate.
#[derive(Clone, Debug, Serialize, Deserialize)]
pub struct GateThresholds {
    /// Minimum composite scalar to Promote (default 0.65).
    pub promote_min: f32,
    /// Minimum composite scalar to Hold (default 0.35).
    pub hold_min: f32,
    /// Minimum KAM stability for promotion (default 0.5).
    pub kappa_min: f32,
    /// Minimum evidence mass for promotion (default 0.2).
    pub eta_min: f32,
}

impl Default for GateThresholds {
    fn default() -> Self {
        Self {
            promote_min: 0.65,
            hold_min: 0.35,
            kappa_min: 0.5,
            eta_min: 0.2,
        }
    }
}

/// Given a signature and optional thresholds, decide the gate action.
///
/// The default logic:
/// - **Promote** if composite ≥ promote_min AND kappa ≥ kappa_min AND eta ≥ eta_min
/// - **Hold** if composite ≥ hold_min
/// - **Reject** otherwise
#[instrument(skip(sig, thresholds))]
pub fn gate_action(sig: &ResonanceSignature, thresholds: Option<&GateThresholds>) -> GateAction {
    let t = thresholds.unwrap_or(&GateThresholds::default());
    let composite = sig.composite_scalar();

    if composite >= t.promote_min && sig.kappa >= t.kappa_min && sig.eta >= t.eta_min {
        info!(composite, "gate action: Promote");
        GateAction::Promote
    } else if composite >= t.hold_min {
        debug!(composite, "gate action: Hold");
        GateAction::Hold
    } else {
        warn!(composite, "gate action: Reject");
        GateAction::Reject
    }
}

// ---------------------------------------------------------------------------
// ResonanceGate — stateful gate with running statistics
// ---------------------------------------------------------------------------

/// Stateful Resonance Gate that maintains running statistics over all
/// evaluated claims.
///
/// This is the production entry-point: agents call `evaluate(claim, context)`
/// and receive a `(GateAction, ResonanceSignature)` pair.
#[derive(Clone, Debug)]
pub struct ResonanceGate {
    thresholds: GateThresholds,
    /// Running mean of composite scores.
    mean_composite: f32,
    /// Running count of evaluations.
    count: u64,
    /// Histogram of actions.
    promotions: u64,
    holds: u64,
    rejections: u64,
}

impl Default for ResonanceGate {
    fn default() -> Self {
        Self {
            thresholds: GateThresholds::default(),
            mean_composite: 0.0,
            count: 0,
            promotions: 0,
            holds: 0,
            rejections: 0,
        }
    }
}

impl ResonanceGate {
    /// Create a new gate with custom thresholds.
    pub fn new(thresholds: GateThresholds) -> Self {
        Self {
            thresholds,
            ..Default::default()
        }
    }

    /// Evaluate a claim against the current context.
    ///
    /// Returns the signature and the resulting gate action, updating
    /// internal running statistics.
    pub fn evaluate(&mut self, claim: &Claim, context: &Context) -> (ResonanceSignature, GateAction) {
        let sig = compute_resonance(claim, context);
        let action = gate_action(&sig, Some(&self.thresholds));

        // Update running statistics (Welford's online algorithm)
        self.count += 1;
        let delta = sig.composite_scalar() - self.mean_composite;
        self.mean_composite += delta / self.count as f32;

        match action {
            GateAction::Promote => self.promotions += 1,
            GateAction::Hold => self.holds += 1,
            GateAction::Reject => self.rejections += 1,
        }

        (sig, action)
    }

    /// Current running mean of all composite scores.
    pub fn mean_composite(&self) -> f32 {
        self.mean_composite
    }

    /// Total number of evaluations.
    pub fn total_evaluations(&self) -> u64 {
        self.count
    }

    /// Histogram of gate actions.
    pub fn action_histogram(&self) -> (u64, u64, u64) {
        (self.promotions, self.holds, self.rejections)
    }

    /// Adjust thresholds based on recent statistics (self-tuning gating).
    ///
    /// If rejection rate is too high, we lower `promote_min` slightly;
    /// if promotion rate is too high, we raise it.
    pub fn auto_tune(&mut self) {
        if self.count == 0 {
            return;
        }
        let rej_rate = self.rejections as f32 / self.count as f32;
        let prom_rate = self.promotions as f32 / self.count as f32;

        if rej_rate > 0.5 {
            self.thresholds.promote_min = (self.thresholds.promote_min * 0.95).max(0.4);
            debug!("auto-tune: lowering promote_min due to high rejection rate");
        } else if prom_rate > 0.7 {
            self.thresholds.promote_min = (self.thresholds.promote_min * 1.05).min(0.9);
            debug!("auto-tune: raising promote_min due to high promotion rate");
        }
    }
}

