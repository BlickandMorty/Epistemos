//! Resonance Gate — epistemic gating via 8-field signature.
//!
//! The Resonance Gate is the philosophical core of the Epistenos runtime.
//! Every claim that enters the system is evaluated against an 8-field
//! signature that determines whether the claim is **Promoted** (accepted
//! into higher residency), **Held** (kept at current level), or **Rejected**
//! (demoted or dropped).
//!
//! The eight fields encode:
//! 1. **Ternary claim state** — does the claim fit, wait, or fall?
//! 2. **Direction** — should we promote, demote, or hold?
//! 3. **Primality** — is this a fundamental or derived claim?
//! 4. **Resonance strength** — how much does it cohere with existing state?
//! 5. **KAM stability** — Kolmogorov-Arnold-Moser invariant measure
//! 6. **Evidence mass** — how much observational support exists?
//! 7. **Residency level** — L0 (sensory) → L_SE (self-evolving)
//! 8. **Learning mode** — which plasticity rule should govern adaptation?

