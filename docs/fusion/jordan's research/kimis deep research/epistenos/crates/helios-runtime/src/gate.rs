/// Direction of change for a claim's residency.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum Direction {
    /// Move the claim to a higher residency level.
    Promote,
    /// Move the claim to a lower residency level.
    Demote,
    /// Keep the claim at its current residency level.
    Hold,
}

/// Primality — distinguishes fundamental (prime) claims from derived
/// (composite) claims.
///
/// Prime claims are axiomatic or directly observed. Composite claims
/// are inferred, deduced, or generated by the model.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum Primality {
    /// A fundamental claim — directly observed or axiomatic.
    Prime,
    /// A derived claim — inferred, composed, or model-generated.
    Composite,
}

/// Residency level — the hierarchical tier at which a claim is held.
///
/// The levels form a cognitive stack from raw sensory input (L0) up
/// to the self-evolving meta-layer (L_SE) where the model modifies
/// its own learning rules.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum Residency {
    /// L0 — raw sensory / token-level input.
    L0,
    /// L1 — local feature / embedding level.
    L1,
    /// L2 — contextual / attention-level representations.
    L2,
    /// L3 — episodic / working-memory level.
    L3,
    /// L4 — long-term / consolidated knowledge.
    L4,
    /// L_SE — self-evolving meta-layer (titans, fast weights, DoRA).
    LSe,
}

impl Residency {
    /// Numeric level for comparison.
    pub fn level(self) -> u8 {
        match self {
            Residency::L0 => 0,
            Residency::L1 => 1,
            Residency::L2 => 2,
            Residency::L3 => 3,
            Residency::L4 => 4,
            Residency::LSe => 5,
        }
    }
}

/// Learning mode — which plasticity rule governs weight adaptation for
/// this claim or agent.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
#[serde(rename_all = "snake_case")]
pub enum LearningMode {
    /// Stochastic Gradient Descent — standard backprop-based update.
    Sgd,
    /// Hebbian learning — "fire together, wire together".
    Hebbian,
    /// Elastic Weight Consolidation — protect important weights.
    Ewc,
    /// Fast weights — online adaptation with separate fast parameters.
    FastWeights,
    /// Meta-learning — learn how to learn (higher-order gradients).
    Meta,
}

/// The action the gate should take after evaluating a signature.
#[derive(Clone, Copy, Debug, PartialEq, Eq, Hash, Serialize, Deserialize)]
pub enum GateAction {
    /// Accept the claim into the target residency.
    Promote,
    /// Keep the claim at its current residency.
    Hold,
    /// Reject the claim (demote or drop).
    Reject,
}

// ---------------------------------------------------------------------------
// ResonanceSignature — the 8-field vector
// ---------------------------------------------------------------------------

/// The full 8-field Resonance Signature that characterises a claim's
/// relationship to the cognitive state.
///
/// Each field is computed by [`compute_resonance`] and consumed by
/// [`gate_action`] to produce a [`GateAction`].
#[derive(Clone, Debug, PartialEq, Serialize, Deserialize)]
pub struct ResonanceSignature {
    /// `tau` — ternary claim state: {-1, 0, +1}.
    pub tau: TernaryState,
    /// `delta` — direction: Promote, Demote, Hold.
    pub delta: Direction,
    /// `pi` — primality: Prime (fundamental) vs Composite (derived).
    pub pi: Primality,
    /// `rho` — resonance strength ∈ [0.0, 1.0].
    /// Measures coherence with existing claims.
    pub rho: f32,
    /// `kappa` — KAM (Kolmogorov-Arnold-Moser) stability ∈ [0.0, 1.0].
    /// Measures whether the claim's addition preserves quasi-periodic
    /// orbits in the epistemic phase space (i.e. does not destabilise
    /// the knowledge manifold).
    pub kappa: f32,
    /// `eta` — evidence mass ∈ [0.0, 1.0].
    /// Fraction of available evidence slots that support this claim.
    pub eta: f32,
    /// `lambda` — residency level (L0 → L_SE).
    pub lambda: Residency,
    /// `learning_mode` — which plasticity rule to apply.
    pub learning_mode: LearningMode,
}

impl ResonanceSignature {
    /// Composite scalar — a single number summarising the signature.
    ///
    /// Weighted: 0.3·rho + 0.3·kappa + 0.2·eta + 0.2·tau_norm,
    /// where `tau_norm` maps {-1,0,+1} → {0.0,0.5,1.0}.
    pub fn composite_scalar(&self) -> f32 {
        let tau_norm = (self.tau.as_i8() as f32 + 1.0) / 2.0;
        0.3 * self.rho + 0.3 * self.kappa + 0.2 * self.eta + 0.2 * tau_norm
    }
}

